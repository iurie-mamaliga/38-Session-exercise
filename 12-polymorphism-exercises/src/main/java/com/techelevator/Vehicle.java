package com.techelevator;

public class Vehicle {
	protected String name;
	protected int distance;
	protected double rate;
	
	//  Constructor
	
	public Vehicle(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public int getDistance() {
		return distance;
	}

	public double getRate() {
		return rate;
	}
	
	

}
